// wtrignometric ratios
#include<iostream>
#include<cmath>
using namespace std;
int main()
{
	long double angle;
    cout<<"Enter the Angle:";cin>>angle;
    cout<<"sin"<<angle<<"= "<<sin(angle)<<endl;
    cout<<"cos"<<angle<<"= "<<cos(angle)<<endl;
    cout<<"tan"<<angle<<"= "<<tan(angle)<<endl;
    cout<<"arcsin"<<angle<<"= "<<asin(angle)<<endl;
    cout<<"arccos"<<angle<<"= "<<acos(angle)<<endl;
    cout<<"arctan"<<angle<<"= "<<atan(angle)<<endl;
	return 0; 
}